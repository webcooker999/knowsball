export const getRandomAgePlayer = (usedIds: string[] = [], baseValue?: number, currentStreak: number = 0): Player => {
  let available = agePlayers.filter(p => !usedIds.includes(p.id) && p.value > 0);

  if (available.length === 0) {
    available = agePlayers.filter(p => p.value > 0); // Reset if all are used
  }

  if (baseValue !== undefined) {
    let gap = 10; 
    if (currentStreak > 5) gap = 8;
    if (currentStreak > 10) gap = 6;
    if (currentStreak > 15) gap = 4;
    if (currentStreak > 20) gap = 3;

    const min = Math.max(0, baseValue - gap);
    const max = baseValue + gap;

    const closePlayers = available.filter(p => p.value >= min && p.value <= max);
    if (closePlayers.length > 0) {
      return closePlayers[Math.floor(Math.random() * closePlayers.length)];
    }
  }

  return available[Math.floor(Math.random() * available.length)];
};
